/*
If-Else Ladder

An if-else ladder is a control structure in programming that allows you to check multiple conditions one after another.

It’s called a ladder because conditions are checked step by step, like climbing down rungs of a ladder:

The program checks the first if condition.

If true → executes its block and skips the rest.

If false → goes to the next condition.

The program then checks the next else if condition.

If true → executes and skips the rest.

If false → goes further down.

Finally, if none of the conditions are true → the else block runs (default case).

*/